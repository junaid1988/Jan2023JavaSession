package javasessions;

public class DataTypeConcept {

	public static void main(String[] args) {
		
		
		// data type:
		
		//1. Primitive DataType: it will take the space in the memory- without object
		
		// boolean type: true/false
		//Numeric Type:
		         //a. character type: char
		         //b. Integral Value: 
		                 //b.1: Integer: bype, short, int, long
		                 //b.2: Floating-point: float, double
		
		//2. Non-primitive data types: With object. Array, String, Classes, Interface, --OOP
		
		//1. byte:
		//size: 1 byte = 8 bits
		//range:: -128 to 127
		byte b =10;
		b=30;
		byte c=20;
		byte g=0;
		byte h = -90;
		System.out.println(b);
		System.out.println(g);
		
		//2. short: 
		//size: 2 bytes = 2x8 = 16 bits
		//range: -32768 to 32767
		short s = 234;
		short s1 = 1;
		System.out.println(s);
		
		
		//3. int:
		//size: 4 bytes = 32 bits
		//range: -2147483648 to 2147483647
		int i = 2300;
		int j = 909998;
		int k = 0; // not recommended 
		System.out.println(j);
		
		//4. long:
		//size: 8 bytes = 64 bits
		//range: -9,223,372,036,854,775,808 to 9,223,372,036,854,775,808
		long num = 12121212121L;
		long phone = 2345678919L;// Not recommended
		System.out.println();
		
		//BigInteger:
		
		//5. float:
		//size: 4 bytes = 32 bits
		//range: after . it can take upto 7 digits
		float f1 = 12.34f;
		float f2 = (float)23.34;
		float f3 = 100; //100.00
		System.out.println(f3);
		
		//6. double 
		//size: 8 bytes = 64 bits
		//range: after . it can take 15 digits
		double d = 12.333;
		double d1 = 1.1;
		System.out.println(d1);
		
		//BigDecimal
		
		//7. char:
		//size: 2 bytes = 16 bits
		//range: 
		char c1 ='a'; //a-z, A-Z
		char c3 = 'A'; 
		char c4 = 'H';
		char c2 = '1'; // 0-9
		char c5 = '$';
		char c6 = '*';
		System.out.println(c5);
		System.out.println(c3);
		
		//0-9
		//a-z
		//A-Z
		//All special type of character
		
		     
		
		       



	}

}
